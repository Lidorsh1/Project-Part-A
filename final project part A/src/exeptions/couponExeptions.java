package exeptions;

public class couponExeptions extends Exception{

	private static final long serialVersionUID = 1L;

	public couponExeptions() {
		super();
	}

	public couponExeptions(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public couponExeptions(String message, Throwable cause) {
		super(message, cause);
	}

	public couponExeptions(String message) {
		super(message);
	}

	public couponExeptions(Throwable cause) {
		super(cause);
	}
	
	
	


}
